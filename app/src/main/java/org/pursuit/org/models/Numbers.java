package org.pursuit.org.models;

public class Numbers {
    private String text;
    private int number;
    private boolean found;
    private String math;

    public String getText() {
        return text;
    }

    public int getNumber() {
        return number;
    }

    public boolean isFound() {
        return found;
    }

    public String getMath() {
        return math;
    }
}
